package lesson7;

public class Animal {
    String name;
    int age;
    double weight;
    int swimming;
    int running;

    public Animal() {

    }

    public Animal(String name, int age, double weight, int swimming, int running) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.swimming = swimming;
        this.running = running;
    }
}
